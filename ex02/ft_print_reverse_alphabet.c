#include <unistd.h>

void	ft_print_reverse_alphabet(void)
{
	char	letter;
	int	i;

	letter = 'z';
	i = 1;
	while (letter >= 'a')
	{
		write(1, &letter, 1);
		letter--;
	}
}

int	main(void)
{
	ft_print_reverse_alphabet();
}
